package de.intarsys.tools.functor;

public class FunctorInternalException extends FunctorException {

	public FunctorInternalException() {
		super();
	}

	public FunctorInternalException(String message) {
		super(message);
	}

	public FunctorInternalException(String message, Throwable cause) {
		super(message, cause);
	}

	public FunctorInternalException(Throwable cause) {
		super(cause);
	}

}
